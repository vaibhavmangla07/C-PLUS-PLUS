using namespace std;
int main()
{
    int a, b;
    cout << "Enter two different numbers: ";
    cin >> a >> b;

    if (a > b)
    {
        cout << a << " is the greater number." << endl;
    }
    else
    {
        cout << b << " is the greater number." << endl;
    }

    return 0;
}
